import UIKit

extension TFFloatingTextField {
    
    enum Constant {}
}

extension TFFloatingTextField.Constant {
    
    enum Font {
        
        static let defaultFontSize: CGFloat = 17
        
        static let regular = "Helvetica Neue"
        static let regularItalic = "HelveticaNeue-Italic"
        static let medium = "HelveticaNeue-Medium"
        static let mediumItalic = "HelveticaNeue-MediumItalic"
        static let bold = "HelveticaNeue-Bold"
        static let boldItalic = "HelveticaNeue-BoldItalic"
    }
}

extension TFFloatingTextField.Constant {
    
    enum Color {
        
        static let defaultFontSize: CGFloat = 17
        
        static let regular = "Helvetica Neue"
        static let regularItalic = "HelveticaNeue-Italic"
        static let medium = "HelveticaNeue-Medium"
        static let mediumItalic = "HelveticaNeue-MediumItalic"
        static let bold = "HelveticaNeue-Bold"
        static let boldItalic = "HelveticaNeue-BoldItalic"
    }
}
